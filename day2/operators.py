#Operators: Arithmetic, Relational, Logical

#Arithmetic Operators
a=5
b=2

print(a+b)
print(a-b)
print(a*b)
print(a**b) #5^2 exponenent value

print(a/b) #2.5
print(a//b) #return Quotient value
print(a%b) #return Remainder
print("------------------------------------------")
##Relational Operators (>, <, <=, >=, !=, ==) Always returns a bool value- True or False
a=5
b=10
print(a>b)
print(a<b)
print(a<=b)
print(a>=b)
print(a!=b)
print(a==b)
print("------------------------------------------")
### Logical Operators (and, or, not)
a= True
b= False
print(a and b) # False
print(a or b) # True
print(not a) # False
print(not b) # True
print("------------------------------------------")